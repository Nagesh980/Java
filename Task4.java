class BoxColors {
    public static void main(String[] args) {
        // Step 1: Initialize an array with 30 colors
        String[] colors = {
            "Red", "Blue", "Green", "Yellow", "Orange", "Purple", "Black", "White", "Pink", "Brown",
            "Gray", "Cyan", "Magenta", "Lime", "Olive", "Maroon", "Navy", "Teal", "Silver", "Gold",
            "Beige", "Turquoise", "Lavender", "Coral", "Indigo", "Violet", "Crimson", "Peach", "Amber", "Chocolate"
        };

        // Step 2: Print all elements using index
        System.out.println("Initial Colors:");
        for (int i = 0; i < colors.length; i++) {
            System.out.println("Box[" + i + "]: " + colors[i]);
        }

        // Step 3: Update all colors to uppercase
        for (int i = 0; i < colors.length; i++) {
            colors[i] = colors[i].toUpperCase();
        }

        // Step 4: Print all updated elements using index
        System.out.println("\nUpdated Colors (Uppercase):");
        for (int i = 0; i < colors.length; i++) {
            System.out.println("Box[" + i + "]: " + colors[i]);
        }
    }
}

